let rend = sounds;

function renderEle() {
  padBank.innerHTML = "";
  rend.forEach((sound) => {
    const newItem = document.createElement("div");
    newItem.classList.add("drum-pad");
    newItem.setAttribute("id", sound.id);
    newItem.onclick = () => soundBtn(newItem);
    newItem.innerHTML = `   <audio
    class="clip"
    id=${sound.keyTrigger}
    src=${sound.url}
  ></audio>
  ${sound.keyTrigger}
  `;
    padBank.appendChild(newItem);
  });
}

function soundBtn(e) {
  if (power === "right") {
    e.classList.add("right");
    const music = e.children[0];
    music.play();
    dis.innerHTML = e.id;
  }
  if (power === "left") {
    e.classList.add("left");
  }
  setInterval(() => {
    e.classList = [];
    e.classList.add("drum-pad");
  }, 200);
}

function detectVolume(e) {
  dis.innerText = `Volume : ${Math.floor(e.value)}`;
  setInterval(() => {
    dis.innerHTML = "&nbsp;";
  }, 1000);
}

function changePower() {
  if (power === "left") {
    power = "right";
    range.removeAttribute("disabled");
  } else {
    power = "left";
    range.setAttribute("disabled", true);
    dis.innerHTML = "&nbsp;";
  }
  powerS.style.float = power;
}

function changeBank() {
  if (power === "right") {
    bank = bank === "right" ? "left" : "right";
    bankS.style.float = bank;
    if (bank === "right") {
      rend = soundsTwo;
      renderEle();
    } else {
      rend = sounds;
      renderEle();
    }
  }
}

renderEle();
